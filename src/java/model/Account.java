package model;

/**
 *
 * @author Admin
 */
public class Account {
    private String user, password;
    private int admin;

    public Account() {
    }

    public Account(String user, String password, int admin) {
        this.user = user;
        this.password = password;
        this.admin = admin;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getAdmin() {
        return admin;
    }

    public void setAdmin(int admin) {
        this.admin = admin;
    }
    
}
